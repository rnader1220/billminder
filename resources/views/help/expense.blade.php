<p>Describe Concept and Display Screen</p>
<p>The form for viewing, creating, and editing have these fields:</p>
<ul>
<li><strong>Name</strong>:  This field is what will display on the list. This is the only required field! (Encrypted)</li>
<li><strong>Current Amount</strong>:  How much is the bill, currently?</li>
<li><strong>Estimated Amt?</strong>:  Is the current amount an estimate, or is it confirmed?</li>
<li><strong>Frequency</strong>:  What is the billing cycle for this bill (monthly, annual, etc)?</li>
<li><strong>Next Due Date</strong>:  When is this bill due, currently?</li>
<li><strong>Estimated Date?</strong>:  Is the due date an estimate, or is it confirmed?</li>
<li><strong>Fixed Amount?</strong>:  Is this a fixed amount payment? Cycling will not reset the estimate flag.</li>
<li><strong>Payments Left?</strong>: Does this have a fixed number of payments?  If set, this value will go down on cycling.</li>
<li><strong>Balance Left?</strong>:  For informational purposes only:  Use to keep track of your balance.</li>
<li><strong>Description</strong>:  Write whatever you like here: description, notes, and so forth, for this account. (Encrypted)</li>
<li><strong>Category</strong>:  This field is what will display on the list. Select from your category list, to organize your bills and income for reporting purposes.  See the Categories tab for more details.</li>
<li><strong>From Account</strong>: This can point to your internal account (bank, etc).  If set, and the account has a link, it will appear here.  See the Accounts tab for more details.</li>
<li><strong>Pay To</strong>: This can point to your external account (cc company, employer, etc).  If set, and the account has a link, it will appear here.  See the Accounts tab for more details.</li>
</ul>
<p>Meaningful fields are encrypted in the database for your protection.  These are indicated above.</p>
